    public static void main(String[] args) throws FileNotFoundException {
        var all = new TreeSet<Class<?>>(Comparator.comparing(Class::getName));
        all.addAll(allClasses);

        System.out.println(all.size());
        var cu = new com.github.javaparser.ast.CompilationUnit();

        for (var clazz : all) {
            var r = new ClassOrInterfaceDeclaration();
            r.setName(clazz.getSimpleName());
            if (clazz.getSuperclass() != null) {
                r.getExtendedTypes().add(new ClassOrInterfaceType(null, clazz.getSuperclass().getSimpleName()));
            }

            if(clazz.accessFlags().contains(AccessFlag.ABSTRACT)) {
                r.addModifier(com.github.javaparser.ast.Modifier.DefaultKeyword.ABSTRACT);
            }

            for (var declaredField : clazz.getDeclaredFields()) {
                if (declaredField.isSynthetic()
                        || declaredField.accessFlags().contains(AccessFlag.STATIC)
                        || declaredField.accessFlags().contains(AccessFlag.PRIVATE))
                    continue;

                String type;
                if(declaredField.getType().getTypeParameters().length==0)
                    type = declaredField.getType().getSimpleName();
                else {
                    type = declaredField.getGenericType().toString();
                    int j = type.lastIndexOf('<');
                    int i = type.lastIndexOf('.', j);
                    type = type.substring(i+1);
                }
                System.out.println(type);
                r.addField(type, declaredField.getName());
            }


            cu.addType(r);
        }

        try (var out = new PrintWriter(new File("key.ncore.java/src/adt/java-ast.java").getAbsoluteFile())) {
            out.write(cu.toString());
        }
    }
